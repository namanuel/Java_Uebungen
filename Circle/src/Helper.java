public class Helper {
    private String mColor1;
    private String mColor2;
    private double mRadius1;
    private double mRadius2;
    public Helper(String mColor1new, String mColor2new, double mRadius1new, double mRadius2new){
        mColor1 = mColor1new;
        mColor2 = mColor2new;
        mRadius1 = mRadius1new;
        mRadius2 = mRadius2new;
    }
    public double getmRadius1(){
        return mRadius1;
    }
    public void setmRadius1(double mRadius1new){
        mRadius1new = mRadius1;
    }
    public double getmRadius2(){
        return mRadius2;
    }
    public void setmRadius2(double mRadius2new){
        mRadius2new = mRadius2;
    }
    public String getmColor1(){
        return mColor1;
    }
    public void setmColor1(String mColor1new){
        mColor1new = mColor1;
    }
    public String getmColor2(){
        return mColor2;
    }
    public void setmColor2(String mColor2new){
        mColor2new = mColor2;
    }


}
